BEFORE_TRADING_TIME = datetime.time(7, 0)
AFTER_TRADING_TIME = datetime.time(15, 0)

STOCK_AM_OPEN = datetime.time(9, 30)
STOCK_AM_CLOSE = datetime.time(11, 30)
STOCK_PM_OPEN = datetime.time(13, 0)
STOCK_PM_CLOSE = datetime.time(15, 0)

FUTURE_AM_OPEN = datetime.time(9, 0)
FUTURE_AM_CLOSE = datetime.time(11, 30)
FUTURE_PM_OPEN = datetime.time(13, 0)
FUTURE_PM_CLOSE = datetime.time(15, 15)


@lru_cache(512)
def trading_time_to_str(trading_time):
    return ', '.join(
        ['%02d:%02d-%02d:%02d' % (divmod(s, 60) + divmod(e, 60))
         for s, e in trading_time])


@lru_cache(512)
def get_trading_schedule(trading_time, is_backtest=True):
    time_set = set()
    start_time_delta = int(is_backtest) # 如果is_backtest为True则为1, False为0
    for s, e in trading_time:
        if s > 1200:
            continue
        for i in range(s+start_time_delta, e+1):
            time_set.add(divmod(i, 60))
    return time_set


def is_stock_trade_time_now(now=None):
    if now is None:
        now = datetime.datetime.now().time()
    return STOCK_AM_OPEN < now < STOCK_AM_CLOSE or STOCK_PM_OPEN < now < STOCK_PM_CLOSE


def is_future_trade_time_now(now=None):
    if now is None:
        now = datetime.datetime.now().time()
    return FUTURE_AM_OPEN < now < FUTURE_AM_CLOSE or FUTURE_PM_OPEN < now < FUTURE_PM_CLOSE
